     public void addRowsTojTable() {
         DefaultTableModel model = (DefaultTableModel) jTable.getModel();
         Object rowData[] = new Object[5];
         MarketingEmployee x = new MarketingEmployee();
         ArrayList<MarketingEmployee> MarketingEmps = new ArrayList<MarketingEmployee>();
        try {
            MarketingEmps = x.listMarketing();
        } catch (ParseException ex) {
            Logger.getLogger(MarketingList.class.getName()).log(Level.SEVERE, null, ex);
        }
         
         for(MarketingEmployee i : MarketingEmps) {
             rowData[0] = i.getId();
             rowData[1] = i.getFname();
             rowData[2] = i.getLname();
             rowData[3] = i.getAge();
             rowData[4] = i.getSalary();
             
             model.addRow(rowData);
         }
     }
