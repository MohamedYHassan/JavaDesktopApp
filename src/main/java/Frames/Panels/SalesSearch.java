    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         if(!jTextFieldSearch.getText().equals("")) {
            Products x = new Products();
            Products returned = x.searchProducts(Integer.parseInt(jTextFieldSearch.getText()));
            if (returned != null) {
                if(returned.checkExpiry(returned))
                    returned.deleteProducts(returned.getId());
                else 
                    addRowTojTable(returned);
                if(checkExpiry(returned))
                    JOptionPane.showMessageDialog(this,"Product " + returned.getId() + " is about to expire!","Warning",JOptionPane.WARNING_MESSAGE);
                if(checkAmount(returned))
                    JOptionPane.showMessageDialog(this,"Product " + returned.getId() + " is about to be out of stock!","Warning",JOptionPane.WARNING_MESSAGE);
                    
                    
            }
            else {
                jLabelSuccessFailResult.setText("Not Found!");
                
            }
        } else {
            jLabelSuccessFailResult.setText("Missing Required Field! Please Search for a Valid Employee");
             }
    }//GEN-LAST:event_jButton1ActionPerformed


