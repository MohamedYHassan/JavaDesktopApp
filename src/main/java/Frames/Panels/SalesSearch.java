    public void addRowTojTable(Products x) {
         DefaultTableModel model = (DefaultTableModel) jTable.getModel();
         Object rowData[] = new Object[6];
         
         
         
             rowData[0] = x.getId();
             rowData[1] = x.getName();
             rowData[2] = x.getDescription();
             rowData[3] = x.getQuantity();
             rowData[4] = x.getPrice();
             rowData[5] = x.getExpiry();
             
             model.addRow(rowData);
         }
    
    
    public boolean checkExpiry(Products x) {
        GregorianCalendar calendar = new GregorianCalendar();
        int month = 1 + calendar.get(GregorianCalendar.MONTH);
        int year = calendar.get(GregorianCalendar.YEAR);
        if(month == 12) {
            if(x.getMonth() == 1)
                if(x.getYear() -1 == year)
                    return true; } 
            else if(x.getYear() == year)
                if(x.getMonth() + 1 == month)
                     return true; 
        System.out.println(year + " " + month);
        return false;
    }
     
    
    public boolean checkAmount(Products x) {
        if(x.getQuantity() < 10)
            return true;
        return false;
    }
    

    
     
      
